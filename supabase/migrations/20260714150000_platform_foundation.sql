create or replace function public.archive_guest_run(p_payload jsonb)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  v_run_id uuid;
  v_client_run_id text := nullif(left(coalesce(p_payload->>'client_run_id', ''), 120), '');
  v_claim_token text := nullif(left(coalesce(p_payload->>'claim_token', ''), 120), '');
begin
  if v_client_run_id is null or v_claim_token is null then
    raise exception 'client_run_id and claim_token are required';
  end if;

  insert into public.guest_runs (
    guest_id,
    nickname,
    device_fingerprint,
    claim_token,
    score,
    cash,
    bank,
    debt,
    health,
    fame,
    coat,
    days_used,
    ended_reason,
    final_state,
    client_run_id,
    session_id,
    city_key,
    city_version,
    game_version,
    share_code,
    is_public
  ) values (
    left(coalesce(p_payload->>'guest_id', 'unknown'), 120),
    left(coalesce(nullif(trim(p_payload->>'nickname'), ''), '匿名玩家'), 24),
    left(coalesce(p_payload->>'device_fingerprint', ''), 160),
    v_claim_token,
    coalesce((p_payload->>'score')::integer, 0),
    coalesce((p_payload->>'cash')::integer, 0),
    coalesce((p_payload->>'bank')::integer, 0),
    coalesce((p_payload->>'debt')::integer, 0),
    coalesce((p_payload->>'health')::integer, 0),
    coalesce((p_payload->>'fame')::integer, 0),
    coalesce((p_payload->>'coat')::integer, 0),
    greatest(0, coalesce((p_payload->>'days_used')::integer, 0)),
    left(coalesce(p_payload->>'ended_reason', 'ended'), 40),
    coalesce(p_payload->'final_state', '{}'::jsonb),
    v_client_run_id,
    nullif(left(coalesce(p_payload->>'session_id', ''), 120), ''),
    left(coalesce(p_payload->>'city_key', 'hangzhou'), 32),
    left(coalesce(p_payload->>'city_version', 'hz-v1'), 80),
    nullif(left(coalesce(p_payload->>'game_version', ''), 120), ''),
    nullif(left(coalesce(p_payload->>'share_code', ''), 120), ''),
    false
  )
  on conflict (client_run_id) where client_run_id is not null
  do update set client_run_id = excluded.client_run_id
  returning id into v_run_id;

  return v_run_id;
end;
$$;

grant execute on function public.archive_guest_run(jsonb) to anon, authenticated;

create or replace view public.leaderboard as
select
  gr.user_id,
  coalesce(p.display_name, '匿名玩家') as display_name,
  p.avatar_url,
  gr.id as run_id,
  gr.score,
  gr.cash,
  gr.bank,
  gr.debt,
  gr.health,
  gr.days_used,
  gr.created_at,
  'account'::text as entry_type,
  null::text as claim_token
from public.game_runs gr
left join public.profiles p on p.id = gr.user_id
where gr.is_public

union all

select
  g.claimed_user_id as user_id,
  g.nickname as display_name,
  null::text as avatar_url,
  g.id as run_id,
  g.score,
  g.cash,
  g.bank,
  g.debt,
  g.health,
  g.days_used,
  g.created_at,
  'guest'::text as entry_type,
  null::text as claim_token
from public.guest_runs g
where g.is_public

order by score desc, created_at asc;

