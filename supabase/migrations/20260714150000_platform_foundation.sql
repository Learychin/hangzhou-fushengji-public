create or replace function public.publish_account_run(p_run_id uuid)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  v_run_id uuid;
begin
  update public.game_runs
  set is_public = true,
      published_at = coalesce(published_at, now())
  where id = p_run_id and user_id = auth.uid()
  returning id into v_run_id;

  if v_run_id is null then
    raise exception 'run not found';
  end if;
  return v_run_id;
end;
$$;

grant execute on function public.publish_account_run(uuid) to authenticated;

create or replace function public.publish_guest_run(
  p_claim_token text,
  p_nickname text
)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  v_run_id uuid;
  v_nickname text;
begin
  v_nickname := left(trim(coalesce(p_nickname, '')), 24);
  if char_length(v_nickname) < 1 then
    raise exception 'nickname is required';
  end if;

  update public.guest_runs
  set nickname = v_nickname,
      is_public = true,
      published_at = coalesce(published_at, now())
  where claim_token = p_claim_token
  returning id into v_run_id;

  if v_run_id is null then
    raise exception 'run not found';
  end if;
  return v_run_id;
end;
$$;

