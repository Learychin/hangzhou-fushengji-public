create or replace function public.resolve_city_by_hint(
  p_country text default null,
  p_region text default null,
  p_city text default null
)
returns table (
  city_key text,
  display_name text,
  content_version text,
  config jsonb,
  matched_by text
)
language sql
stable
security definer
set search_path = public
as $$
  with matched as (
    select
      c.city_key,
      c.display_name,
      c.content_version,
      c.config,
      case
        when coalesce(p_city, '') <> '' and exists (
          select 1 from unnest(r.city_patterns) pattern
          where lower(p_city) like '%' || lower(pattern) || '%'
        ) then 'city'
        when coalesce(p_region, '') <> '' and exists (
          select 1 from unnest(r.region_patterns) pattern
          where lower(p_region) like '%' || lower(pattern) || '%'
        ) then 'region'
        else 'country'
      end as matched_by,
      r.priority,
      case
        when coalesce(p_city, '') <> '' and exists (
          select 1 from unnest(r.city_patterns) pattern
          where lower(p_city) like '%' || lower(pattern) || '%'
        ) then 0
        when coalesce(p_region, '') <> '' and exists (
          select 1 from unnest(r.region_patterns) pattern
          where lower(p_region) like '%' || lower(pattern) || '%'
        ) then 1
        else 2
      end as match_rank
    from public.city_route_rules r
    join public.cities c on c.city_key = r.city_key
    where r.enabled and c.enabled
      and (
        coalesce(p_city, '') <> ''
        and exists (
          select 1 from unnest(r.city_patterns) pattern
          where lower(p_city) like '%' || lower(pattern) || '%'
        )
        or coalesce(p_region, '') <> ''
        and exists (
          select 1 from unnest(r.region_patterns) pattern
          where lower(p_region) like '%' || lower(pattern) || '%'
        )
        or coalesce(p_country, '') <> ''
        and upper(p_country) = any(r.country_codes)
      )
    order by match_rank asc, r.priority asc, r.id asc
    limit 1
  )
  select city_key, display_name, content_version, config, matched_by from matched
  union all
  select c.city_key, c.display_name, c.content_version, c.config, 'default'
  from public.cities c
  where c.enabled and c.is_default and not exists (select 1 from matched)
  limit 1;
$$;

grant execute on function public.resolve_city_by_hint(text, text, text) to anon, authenticated;

create or replace function public.record_campaign_event(
  p_campaign_id uuid,
  p_event_type text,
  p_city_key text default 'hangzhou',
  p_session_id text default null,
  p_client_run_id text default null,
  p_guest_id text default null,
  p_metadata jsonb default '{}'::jsonb
)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  v_id bigint;
begin
  if p_event_type not in ('eligible', 'impression', 'click', 'dismiss') then
    raise exception 'invalid campaign event type';
  end if;

  if not exists (
    select 1 from public.active_campaigns ac where ac.id = p_campaign_id
  ) then
    raise exception 'campaign is not active';
  end if;

  insert into public.campaign_events (
    campaign_id,
    event_type,
    city_key,
    session_id,
    client_run_id,
    guest_id,
    user_id,
    metadata
  ) values (
    p_campaign_id,
    p_event_type,
    coalesce(nullif(p_city_key, ''), 'hangzhou'),
    nullif(left(coalesce(p_session_id, ''), 120), ''),
    nullif(left(coalesce(p_client_run_id, ''), 120), ''),
    nullif(left(coalesce(p_guest_id, ''), 120), ''),
    auth.uid(),
    coalesce(p_metadata, '{}'::jsonb)
  )
  returning id into v_id;

  return v_id;
end;
$$;

grant execute on function public.record_campaign_event(uuid, text, text, text, text, text, jsonb)
  to anon, authenticated;

create or replace function public.admin_overview()
returns jsonb
language sql
stable
security definer
set search_path = public
as $$
  select case
    when not public.is_admin() then
      jsonb_build_object('error', 'forbidden')
    else
      jsonb_build_object(
        'users', (select count(*) from auth.users),
        'profiles', (select count(*) from public.profiles),
        'account_runs', (select count(*) from public.game_runs),
        'guest_runs', (select count(*) from public.guest_runs),
        'game_runs', (
          (select count(*) from public.game_runs)
          + (select count(*) from public.guest_runs)
        ),
        'players_with_runs', (
          (select count(distinct user_id) from public.game_runs)
          + (select count(distinct guest_id) from public.guest_runs where claimed_user_id is null)
        ),
        'events', (
          (select count(*) from public.game_events)
          + (select coalesce(sum(
              case when jsonb_typeof(final_state->'events') = 'array'
                then jsonb_array_length(final_state->'events') else 0 end
            ), 0) from public.guest_runs)
        ),
        'best_score', (
          select coalesce(max(score), 0) from (
            select score from public.game_runs
            union all
            select score from public.guest_runs
          ) scores
        ),
        'avg_score', (
          select coalesce(round(avg(score))::integer, 0) from (
            select score from public.game_runs
            union all
            select score from public.guest_runs
          ) scores
        ),
        'runs_today', (
          select count(*) from (
            select created_at from public.game_runs
            union all
            select created_at from public.guest_runs
          ) runs where created_at >= date_trunc('day', now())
        ),
        'cities', (select count(*) from public.cities),
        'active_campaigns', (select count(*) from public.active_campaigns),
        'campaign_impressions', (
          select count(*) from public.campaign_events where event_type = 'impression'
        ),
        'campaign_clicks', (
          select count(*) from public.campaign_events where event_type = 'click'
        )
      )
  end;
$$;

create or replace function public.admin_players_v2()
returns table (
  player_key text,
  source text,
  email text,
  display_name text,
  created_at timestamptz,
  last_seen_at timestamptz,
  run_count bigint,
  best_score integer
)
language sql
stable
security definer
set search_path = public
as $$
  select * from (
    select
      'account:' || u.id::text as player_key,
      'account'::text as source,
      u.email,
      coalesce(p.display_name, split_part(u.email, '@', 1), '匿名玩家') as display_name,
      u.created_at,
      greatest(u.last_sign_in_at, max(gr.created_at)) as last_seen_at,
      count(gr.id) as run_count,
      coalesce(max(gr.score), 0)::integer as best_score
    from auth.users u
    left join public.profiles p on p.id = u.id
    left join public.game_runs gr on gr.user_id = u.id
    where public.is_admin()
    group by u.id, p.display_name

    union all

    select
      'guest:' || g.guest_id as player_key,
      'guest'::text as source,
      null::text as email,
      max(g.nickname) as display_name,
      min(g.created_at) as created_at,
      max(g.created_at) as last_seen_at,
      count(*) as run_count,
      max(g.score)::integer as best_score
    from public.guest_runs g
    where public.is_admin() and g.claimed_user_id is null
    group by g.guest_id
  ) players
  order by last_seen_at desc nulls last
  limit 500;
$$;

create or replace function public.admin_runs_v2()
returns table (
  run_key text,
  id uuid,
  source text,
  user_id uuid,
  email text,
  display_name text,
  city_key text,
  city_version text,
  game_version text,
  score integer,
  cash integer,
  bank integer,
  debt integer,
  health integer,
  fame integer,
  days_used integer,
  ended_reason text,
  event_count bigint,
  final_state jsonb,
  created_at timestamptz
)
language sql
stable
security definer
set search_path = public
as $$
  select * from (
    select
      'account:' || gr.id::text as run_key,
      gr.id,
      'account'::text as source,
      gr.user_id,
      u.email,
      coalesce(p.display_name, split_part(u.email, '@', 1), '匿名玩家') as display_name,
      gr.city_key,
      gr.city_version,
      gr.game_version,
      gr.score,
      gr.cash,
      gr.bank,
      gr.debt,
      gr.health,
      gr.fame,
      gr.days_used,
      gr.ended_reason,
      count(ge.id) as event_count,
      gr.final_state,
      gr.created_at
    from public.game_runs gr
    join auth.users u on u.id = gr.user_id
    left join public.profiles p on p.id = gr.user_id
    left join public.game_events ge on ge.run_id = gr.id
    where public.is_admin()
    group by gr.id, u.email, p.display_name

    union all

    select
      'guest:' || g.id::text as run_key,
      g.id,
      'guest'::text as source,
      g.claimed_user_id as user_id,
      null::text as email,
      g.nickname as display_name,
      g.city_key,
      g.city_version,
      g.game_version,
      g.score,
      g.cash,
      g.bank,
      g.debt,
      g.health,
      g.fame,
      g.days_used,
      g.ended_reason,
      case when jsonb_typeof(g.final_state->'events') = 'array'
        then jsonb_array_length(g.final_state->'events') else 0 end::bigint as event_count,
      g.final_state,
      g.created_at
    from public.guest_runs g
    where public.is_admin()
  ) runs
  order by created_at desc
  limit 500;
$$;

