create or replace function public.admin_upsert_city(
  p_city_key text,
  p_display_name text,
  p_enabled boolean,
  p_is_default boolean,
  p_content_version text,
  p_config jsonb,
  p_country_codes text[] default array['CN']::text[],
  p_region_patterns text[] default '{}'::text[],
  p_city_patterns text[] default '{}'::text[],
  p_priority integer default 100
)
returns text
language plpgsql
security definer
set search_path = public
as $$
declare
  v_key text := lower(trim(coalesce(p_city_key, '')));
begin
  if not public.is_admin() then raise exception 'forbidden'; end if;
  if v_key !~ '^[a-z0-9][a-z0-9_-]{1,31}$' then raise exception 'invalid city key'; end if;
  if trim(coalesce(p_display_name, '')) = '' then raise exception 'display name is required'; end if;

  if p_is_default then update public.cities set is_default = false where is_default; end if;
  insert into public.cities (
    city_key, display_name, enabled, is_default, content_version, config, updated_at
  ) values (
    v_key,
    left(trim(p_display_name), 80),
    coalesce(p_enabled, false),
    coalesce(p_is_default, false),
    left(coalesce(nullif(trim(p_content_version), ''), 'v1'), 80),
    coalesce(p_config, '{}'::jsonb),
    now()
  )
  on conflict (city_key) do update set
    display_name = excluded.display_name,
    enabled = excluded.enabled,
    is_default = excluded.is_default,
    content_version = excluded.content_version,
    config = excluded.config,
    updated_at = now();

  insert into public.city_route_rules (
    city_key, priority, enabled, country_codes, region_patterns, city_patterns, updated_at
  ) values (
    v_key,
    greatest(1, coalesce(p_priority, 100)),
    coalesce(p_enabled, false),
    coalesce(p_country_codes, array['CN']::text[]),
    coalesce(p_region_patterns, '{}'::text[]),
    coalesce(p_city_patterns, '{}'::text[]),
    now()
  )
  on conflict (city_key) do update set
    priority = excluded.priority,
    enabled = excluded.enabled,
    country_codes = excluded.country_codes,
    region_patterns = excluded.region_patterns,
    city_patterns = excluded.city_patterns,
    updated_at = now();

  return v_key;
end;
$$;

