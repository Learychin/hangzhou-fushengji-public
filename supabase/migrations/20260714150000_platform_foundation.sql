    union all

    select
      'guest-event:' || g.id::text || ':' || (e.ordinality - 1)::text as id,
      'guest:' || g.id::text as run_key,
      'guest'::text as source,
      g.claimed_user_id as user_id,
      case when coalesce(e.value->>'event_index', '') ~ '^\d+$'
        then (e.value->>'event_index')::integer else (e.ordinality - 1)::integer end as event_index,
      coalesce(nullif(e.value->>'event_type', ''), 'log') as event_type,
      case when coalesce(e.value->>'day', '') ~ '^\d+$'
        then (e.value->>'day')::integer else 0 end as day,
      left(coalesce(e.value->>'message', ''), 240) as message,
      case when jsonb_typeof(e.value->'state') = 'object' then e.value->'state' else '{}'::jsonb end as state,
      case when jsonb_typeof(e.value->'payload') = 'object' then e.value->'payload' else '{}'::jsonb end as payload,
      g.created_at
    from public.guest_runs g
    cross join lateral jsonb_array_elements(
      case when jsonb_typeof(g.final_state->'events') = 'array'
        then g.final_state->'events' else '[]'::jsonb end
    ) with ordinality as e(value, ordinality)
    where public.is_admin()
      and p_run_key = 'guest:' || g.id::text
  ) events
  order by event_index asc
  limit 2000;
$$;

create or replace function public.admin_event_summary_v2()
returns table (
  event_type text,
  event_count bigint,
  player_count bigint,
  run_count bigint,
  last_seen_at timestamptz
)
language sql
stable
security definer
set search_path = public
as $$
  with all_events as (
    select
      ge.event_type,
      'account:' || ge.user_id::text as player_key,
      'account:' || ge.run_id::text as run_key,
      ge.created_at
    from public.game_events ge
    where public.is_admin()

    union all

    select
      coalesce(nullif(e.value->>'event_type', ''), 'log') as event_type,
      'guest:' || g.guest_id as player_key,
      'guest:' || g.id::text as run_key,
      g.created_at
    from public.guest_runs g
    cross join lateral jsonb_array_elements(
      case when jsonb_typeof(g.final_state->'events') = 'array'
        then g.final_state->'events' else '[]'::jsonb end
    ) as e(value)
    where public.is_admin()
  )
  select
    event_type,
    count(*) as event_count,
    count(distinct player_key) as player_count,
    count(distinct run_key) as run_count,
    max(created_at) as last_seen_at
  from all_events
  group by event_type
  order by event_count desc, event_type asc;
$$;

